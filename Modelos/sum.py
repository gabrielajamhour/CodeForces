# Criar uma matriz de exemplo
matriz = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

# Índice da sublista a ser somada
indice_sublista = 1

# Somar os elementos da sublista
soma = sum(matriz[indice_sublista])

# Imprimir o resultado
print(f"A soma dos elementos da sublista {indice_sublista} é: {soma}")
